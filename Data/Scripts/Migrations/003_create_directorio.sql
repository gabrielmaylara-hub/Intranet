CREATE TABLE IF NOT EXISTS directorio_areas (
    id        INT          NOT NULL AUTO_INCREMENT,
    nombre    VARCHAR(180) NOT NULL,
    titular   VARCHAR(180) NULL,
    ubicacion VARCHAR(250) NULL,
    correo    VARCHAR(180) NULL,
    orden     INT          NOT NULL DEFAULT 0,
    activo    TINYINT(1)   NOT NULL DEFAULT 1,
    PRIMARY KEY (id),
    UNIQUE KEY uk_directorio_areas_nombre (nombre),
    INDEX idx_directorio_areas_activo_orden (activo, orden)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

